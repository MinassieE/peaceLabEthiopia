# Assignment operators
x = 10
print("x =", x)
x += 5  # x = x + 5
print("x after += 5:", x)
x -= 3
print("x after -= 3:", x)

# Arithmetic operators
a = 7
b = 2
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)

# Comparison operators
print(a == b)   # False
print(a != b)   # True
print(a > b)    # True
print(a <= b)   # False

# Logical operators
print(True and False)  # False
print(True or False)   # True
print(not True)        # False
