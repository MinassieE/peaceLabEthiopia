# Variables store data that we can use later.
# We can name variables with letters, numbers, and underscores (but not start with a number).

# Creating variables
name = "Alice"
age = 20

# Output variables
print("Name:", name)
print("Age:", age)

# You can change these values and run again
name = "Bob"
age = 25
print("Updated Name:", name)
print("Updated Age:", age)

# Multiple assignment
x, y, z = 5, 10, 15
print(x, y, z)
